    private void btnagrandirMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_btnagrandirMouseClicked
    {//GEN-HEADEREND:event_btnagrandirMouseClicked
      ImageIcon agrandir = new  ImageIcon(getClass().getResource("/icon/agrandir.png"));
      ImageIcon minimize = new  ImageIcon(getClass().getResource("/icon/minimize.png"));
      if (getExtendedState() == this.MAXIMIZED_BOTH)
        {
          setExtendedState(this.NORMAL);
          btnagrandir.setIcon(agrandir);
        }
      else
        {
          setExtendedState(this.MAXIMIZED_BOTH);
          btnagrandir.setIcon(minimize);
        }
    }//GEN-LAST:event_btnagrandirMouseClicked

